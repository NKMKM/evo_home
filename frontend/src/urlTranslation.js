// Функция для получения URL по ключу и языку
export const getUrlByKey = (key, language) => {
  return urlTranslations[key]?.[language] || urlTranslations[key]?.['en'] || '/';
};

// Функция для получения ключа по URL и языку
export const getKeyByUrl = (url, language) => {
  for (const [key, translations] of Object.entries(urlTranslations)) {
    if (translations[language] === url) {
      return key;
    }
  }
  return null;
};